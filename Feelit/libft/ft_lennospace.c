#include "libft.h"

size_t		ft_lennospace(char const *s)
{
	size_t len;
	int i;

	len = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != ' ' && s[i] != '\n' && s[i] != '\t')
			len++;
		i++;
	}
	return (len);
}
